#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Currency
//  Description      : Contains function to convert US Dollar into Indian Rupees.
//  Input            : Integer iNo
//  Output           : Returns amount in Indian Rupees
//  Author           : Gavade Somanath Vilas
//  Date             : 15/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Currency
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Currency (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : Integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Currency(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Convert
//  Description      : Converts US Dollar into Indian Rupees (1$ = 70 Rs).
//  Input            : None (Uses class data member)
//  Output           : Returns converted amount
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        int Convert()
        {
            int iConvert = 0;

            if(iNo < 0)
            {
                iNo = -(iNo);
            }

            iConvert = iNo * 70;
            
            return iConvert;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Currency Conversion using OOP
//  Description      : This application accepts amount in US Dollar from user, creates an object of Currency class,
//                     and returns the equivalent amount in Indian Rupees.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout << "Enter Amount (US) : \n";
    cin >> iValue;

    Currency pobj(iValue);

    iRet = pobj.Convert();

    cout << "Indian Currency is : " << iRet << "\n";

    return 0;
}