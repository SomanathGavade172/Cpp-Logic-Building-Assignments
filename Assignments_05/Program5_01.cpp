/*
    1.Write a program which accept number from user and print that number of( $ & * )on screen.

    Input   : 5
    Output  : $ * $ * $ * $ * $ *

    Input   : 3
    Output  : $ * $ * $ *

    Input   : -3
    Output  : $ * $ * $ *
*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Pattern
//  Description      : Contains function to print pattern of $ and * on screen.
//  Input            : Integer iNo - Number of times the pattern should be printed
//  Output           : Prints pattern on screen
//  Author           : Gavade Somanath Vilas
//  Date             : 13/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Pattern
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Pattern (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : Integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Pattern(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Prints pattern of $ and * according to the given number.
//  Input            : None (Uses class data member)
//  Output           : Displays pattern on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        void Display()
        {
            int iCnt = 0;

            for(iCnt = 1; iCnt <= iNo; iCnt++)
            {
                cout << "$\t*\t";
            }

            cout << "\n";
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Pattern Printing using OOP
//  Description      : This application accepts a number from user, creates an object of Pattern class,
//                     and prints pattern of $ and * on screen.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Pattern pobj(iValue);

    pobj.Display();

    return 0;
}