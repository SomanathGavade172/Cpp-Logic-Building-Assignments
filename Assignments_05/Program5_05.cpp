/* 
    5. Write a program which accept N and print first 5 multiples of N.

    Input  : 4
    
    Output : 4 8 12 16 20

*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : PrintMultiples
//  Description      : Contains function to print first five multiples of the given number.
//  Input            : Integer iNo - Number whose multiples should be printed
//  Output           : Prints multiples on screen
//  Author           : Gavade Somanath Vilas
//  Date             : 13/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class PrintMultiples
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : PrintMultiples (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : Integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        PrintMultiples(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Prints first five multiples of the given number.
//  Input            : None (Uses class data member)
//  Output           : Displays multiples on screen
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        void Display()
        {
            int iCnt = 0;

            if(iNo < 0)
            {
                iNo = -(iNo);
            }

            for(iCnt = 1; iCnt <= 5; iCnt++)
            {
                cout << iNo * iCnt << "\t";
            }

            cout << "\n";
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Print First Five Multiples using OOP
//  Description      : This application accepts a number from user, creates an object of PrintMultiples class,
//                     and prints first five multiples of the given number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    PrintMultiples pobj(iValue);

    pobj.Display();

    return 0;
}