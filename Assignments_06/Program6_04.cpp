#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Table
//  Description      : Contains function to display multiplication table of given number.
//  Input            : Integer iNo
//  Output           : Prints table of the given number on screen
//  Author           : Gavade Somanath Vilas
//  Date             : 14/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Table
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Table (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : Integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Table(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Prints multiplication table of the given number.
//  Input            : None (Uses class data member)
//  Output           : Displays table on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        void Display()
        {
            int iCnt = 0;

            if(iNo < 0)
            {
                iNo = -(iNo);
            }

            for(iCnt = 1; iCnt <= 10; iCnt++)
            {
                cout << iNo * iCnt << "\t";
            }

            cout << "\n";
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Multiplication Table using OOP
//  Description      : This application accepts a number from user, creates an object of Table class,
//                     and displays multiplication table of the given number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Table tobj(iValue);

    tobj.Display();

    return 0;
}