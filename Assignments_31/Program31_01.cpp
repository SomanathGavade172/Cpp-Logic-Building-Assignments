#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : String
//  Description      : Contains function to copy string into another string in reverse order.
//  Input            : Character array (string)
//  Output           : Stores reversed string in destination array
//  Author           : Gavade Somanath Vilas
//  Date             : 17/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class String
{
    public:
        char *start;
        char *end;
        char *dest;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : String (Parameterized Constructor)
//  Description      : Initializes data members with given source and destination strings.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        String(char *src, char *d)
        {
            start = src;
            end = src;
            dest = d;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : StrCpyX
//  Description      : Copies characters of source string into destination string in reverse order.
//  Input            : None
//  Output           : Reversed string stored in destination
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void StrCpyX()
        {
            while(*end != '\0')
            {
                end++;
            }

            end--;

            while(start <= end)
            {
                *dest = *end;
                end--;
                dest++;
            }

            *dest = '\0';
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Reverse String Copy using OOP
//  Description      : This application accepts string from user and copies it into another string in reverse order.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];
    char Brr[30];

    cout << "Enter a String : \n";
    cin.getline(Arr, 30);

    String sobj(Arr, Brr);

    sobj.StrCpyX();

    cout << Brr << "\n";

    return 0;
}