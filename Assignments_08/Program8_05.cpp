/*  
    5. Write a program which accept area in square feet and convert it into square meter. (1 square feet = 0.0929 Square meter)

    Input  : 5
    Output : 0.464515

    Input  : 7
    Output : 0.650321

*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Area
//  Description      : Contains function to convert square feet into square meter.
//  Input            : Float fArea
//  Output           : Returns area in square meter
//  Author           : Gavade Somanath Vilas
//  Date             : 16/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Area
{
    public:
        float fArea;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Area (Parameterized Constructor)
//  Description      : Initializes data member with given area in square feet.
//  Input            : Float
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Area(float A)
        {
            fArea = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : SquareMeter
//  Description      : Converts square feet into square meter using formula (1 sq.ft = 0.0929 sq.m).
//  Input            : None (Uses class data member)
//  Output           : Returns area in square meter
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        double SquareMeter()
        {
            double dConvert = 0.0;

            dConvert = fArea * 0.0929;  

            return dConvert;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Square Feet to Square Meter Conversion using OOP
//  Description      : This application accepts area in square feet from user, creates an object of Area class,
//                     and returns the area in square meter.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet = 0.0f;

    cout << "Enter Area in Square Feet : \n";
    cin >> fValue;

    Area aobj(fValue);

    dRet = aobj.SquareMeter();

    cout << fValue << " Square Feet is = " << dRet << " Square Meter.\n";

    return 0;
}