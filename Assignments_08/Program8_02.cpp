#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Rectangle
//  Description      : Contains function to calculate area of rectangle.
//  Input            : Float fWidth, Float fHeight
//  Output           : Returns area of rectangle
//  Author           : Gavade Somanath Vilas
//  Date             : 16/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Rectangle
{
    public:
        float fWidth;
        float fHeight;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Rectangle (Parameterized Constructor)
//  Description      : Initializes data members with given width and height values.
//  Input            : Float, Float
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Rectangle(float A, float B)
        {
            fWidth = A;
            fHeight = B;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : RectArea
//  Description      : Calculates area of rectangle using formula (Width * Height).
//  Input            : None (Uses class data members)
//  Output           : Returns area of rectangle
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        double RectArea()
        {
            double dArea = 0.0;

            dArea = fWidth * fHeight;

            return dArea;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Area of Rectangle using OOP
//  Description      : This application accepts width and height from user, creates an object of Rectangle class,
//                     and returns the area of the rectangle.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue1 = 0.0f, fValue2 = 0.0f;
    double dRet = 0.0;

    cout << "Enter Width : \n";
    cin >> fValue1;

    cout << "Enter Height : \n";
    cin >> fValue2;

    Rectangle robj(fValue1, fValue2);

    dRet = robj.RectArea();

    cout << "Area of a Rectangle is : " << dRet << "\n";

    return 0;
}