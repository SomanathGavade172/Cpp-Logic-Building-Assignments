/* 
    1. Accept number from user and display below pattern.

    Input  : 5

    Output : A B C D E
*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Patter
//  Description      : Contains function to print alphabet pattern.
//  Input            : Integer value
//  Output           : Prints pattern on screen
//  Author           : Gavade Somanath Vilas
//  Date             : 30/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Patter
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Patter (Parameterized Constructor)
//  Description      : Initializes data member with given value.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Patter(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Prints alphabet pattern from A onwards for given number of times.
//  Input            : None (Uses class data member)
//  Output           : Prints pattern on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Display()
        {
            int iCnt = 0;
            char Ch = 'A';

            for(iCnt = 1; iCnt <= iNo; iCnt++, Ch++)
            {
                cout << Ch << "\t";
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Alphabet pattern printing using OOP
//  Description      : This application accepts number from user, creates an object of Patter class and prints alphabet pattern.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a number : \n";
    cin >> iValue;

    Patter pobj(iValue);

    pobj.Display();

    return 0;
}