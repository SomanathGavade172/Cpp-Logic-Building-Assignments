/* 
    2. Accept amount in US Dollar and return its corresponding value in Indian currency.
       Consider 1 Dollar = 70 Rupees.

    Input  : 10
    Output : 700

    Input  : 3
    Output : 210

    Input  : 1200
    Output : 84000
    
*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Currency
//  Description      : Contains function to convert Dollar into Rupees.
//  Input            : Integer value
//  Output           : Returns amount in Rupees
//  Author           : Gavade Somanath Vilas
//  Date             : 29/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Currency
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Currency (Parameterized Constructor)
//  Description      : Initializes data member with given value.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Currency(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Convert
//  Description      : Converts Dollar into Rupees.
//  Input            : None (Uses class data member)
//  Output           : Returns converted amount
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        int Convert()
        {
            int iAns = 0;

            if(iNo < 0)
            {
                iNo = -iNo;
            }

            iAns = iNo * 70;

            return iAns;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Dollar to Rupees conversion using OOP
//  Description      : This application accepts amount in Dollar, creates an object of Currency class and returns amount in Rupees.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout << "Enter amount in Dollar : \n";
    cin >> iValue;

    Currency cobj(iValue);

    iRet = cobj.Convert();

    cout << "Amount in Rupees is : " << iRet << "\n";

    return 0;
}