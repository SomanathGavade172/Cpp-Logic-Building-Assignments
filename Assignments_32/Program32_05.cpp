/* 
    5. Write a program which checks whether first and last bit is On or OFF. First bit means bit number 1 and last bit means bit number 32.

*/

#include<iostream>
using namespace std;

typedef unsigned int UINT;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Bit
//  Description      : Contains function to check whether first and last bits of a number are ON or OFF.
//  Input            : Unsigned Integer
//  Output           : Returns TRUE if both bits are ON, otherwise FALSE
//  Author           : Gavade Somanath Vilas
//  Date             : 03/05/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Bit
{
    public:
        UINT iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Bit (Parameterized Constructor)
//  Description      : Initializes data member with given number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Bit(UINT A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CheckBit
//  Description      : Checks whether first and last bits are ON or OFF.
//  Input            : None
//  Output           : TRUE if both bits are ON, FALSE otherwise
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        bool CheckBit()
        {
            UINT iResult = 0;
            UINT iMask = 0x80000001;

            iResult = iNo & iMask;

            if(iResult == iMask)
            {
                return true;
            }
            else
            {
                return false;
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Check First and Last Bit using OOP
//  Description      : This application accepts a number and checks whether its first and last bits are ON or OFF.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iValue = 0;
    bool bRet = false;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Bit bobj(iValue);

    bRet = bobj.CheckBit();

    if(bRet == true)
    {
        cout << "1st & 32nd bits are ON\n";
    }
    else
    {
        cout << "1st & 32nd bits are OFF\n";
    }

    return 0;
}