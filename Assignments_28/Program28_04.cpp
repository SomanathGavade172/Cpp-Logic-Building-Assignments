#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : String
//  Description      : Contains function to display only digits from the string.
//  Input            : Character array (string)
//  Output           : Displays digits from the string
//  Author           : Gavade Somanath Vilas
//  Date             : 26/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class String
{
    public:
        char *src;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : String (Parameterized Constructor)
//  Description      : Initializes data member with given string.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        String(char *s)
        {
            src = s;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : DisplayDigit
//  Description      : Displays only digits from the given string.
//  Input            : None
//  Output           : Prints digits on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void DisplayDigit()
        {
            while(*src != '\0')
            {
                if(*src >= '0' && *src <= '9')
                {
                    cout << *src;
                }
                src++;
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Display Digits from String using OOP
//  Description      : This application accepts string from user and displays only digits from that string.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];
    
    cout << "Enter a String : \n";
    cin.getline(Arr, 30);

    String sobj(Arr);

    sobj.DisplayDigit();

    return 0;
}