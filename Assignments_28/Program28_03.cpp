#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : String
//  Description      : Contains function to toggle the case of string.
//  Input            : Character array (string)
//  Output           : Converts string into toggle case
//  Author           : Gavade Somanath Vilas
//  Date             : 26/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class String
{
    public:
        char *src;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : String (Parameterized Constructor)
//  Description      : Initializes data member with given string.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        String(char *s)
        {
            src = s;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : strtoggleX
//  Description      : Toggles the case of all alphabets in the string.
//  Input            : None
//  Output           : Modified string (toggle case)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void strtoggleX()
        {
            while(*src != '\0')
            {
                if(*src >= 'a' && *src <= 'z')
                {
                    *src = *src - 32;
                }
                else if(*src >= 'A' && *src <= 'Z')
                {
                    *src = *src + 32;
                }

                src++;
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : String Toggle Case Conversion using OOP
//  Description      : This application accepts string from user and toggles the case of all alphabets.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];
    
    cout << "Enter a String : \n";
    cin.getline(Arr, 30);

    String sobj(Arr);

    sobj.strtoggleX();

    cout << Arr << "\n";

    return 0;
}