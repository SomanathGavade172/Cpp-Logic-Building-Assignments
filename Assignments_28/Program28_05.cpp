#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : String
//  Description      : Contains function to count number of white spaces from the string.
//  Input            : Character array (string)
//  Output           : Returns count of white spaces
//  Author           : Gavade Somanath Vilas
//  Date             : 26/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class String
{
    public:
        char *src;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : String (Parameterized Constructor)
//  Description      : Initializes data member with given string.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        String(char *s)
        {
            src = s;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CountWhite
//  Description      : Counts number of white spaces in the given string.
//  Input            : None
//  Output           : Returns count of white spaces
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        int CountWhite()
        {
            int iCount = 0;

            while(*src != '\0')
            {
                if(*src == ' ')
                {
                    iCount++;
                }
                src++;
            }

            return iCount;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Count White Spaces from String using OOP
//  Description      : This application accepts string from user and counts number of white spaces.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[100];
    int iRet = 0;
    
    cout << "Enter a String : \n";
    cin.getline(Arr, 100);

    String sobj(Arr);

    iRet = sobj.CountWhite();

    cout << "Number of White Spaces are : " << iRet << "\n";

    return 0;
}