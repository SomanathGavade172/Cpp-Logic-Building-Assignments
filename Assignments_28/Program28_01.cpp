#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : String
//  Description      : Contains function to convert string into lower case.
//  Input            : Character array (string)
//  Output           : Converts string into lower case
//  Author           : Gavade Somanath Vilas
//  Date             : 26/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class String
{
    public:
        char *src;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : String (Parameterized Constructor)
//  Description      : Initializes data member with given string.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        String(char *s)
        {
            src = s;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : strlwrX
//  Description      : Converts all capital characters of string into lower case.
//  Input            : None
//  Output           : Modified string (lowercase)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void strlwrX()
        {
            while(*src != '\0')
            {
                if(*src >= 'A' && *src <= 'Z')
                {
                    *src = *src + 32;
                }

                src++;
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : String Lowercase Conversion using OOP
//  Description      : This application accepts string from user and converts it into lower case.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];
    
    cout << "Enter a String : \n";
    cin.getline(Arr, 30);

    String sobj(Arr);

    sobj.strlwrX();

    cout << Arr << "\n";

    return 0;
}