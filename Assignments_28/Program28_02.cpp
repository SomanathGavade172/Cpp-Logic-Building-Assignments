#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : String
//  Description      : Contains function to convert string into upper case.
//  Input            : Character array (string)
//  Output           : Converts string into upper case
//  Author           : Gavade Somanath Vilas
//  Date             : 26/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class String
{
    public:
        char *src;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : String (Parameterized Constructor)
//  Description      : Initializes data member with given string.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        String(char *s)
        {
            src = s;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : struprX
//  Description      : Converts all small characters of string into upper case.
//  Input            : None
//  Output           : Modified string (uppercase)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void struprX()
        {
            while(*src != '\0')
            {
                if(*src >= 'a' && *src <= 'z')
                {
                    *src = *src - 32;
                }

                src++;
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : String Uppercase Conversion using OOP
//  Description      : This application accepts string from user and converts it into upper case.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];
    
    cout << "Enter a String : \n";
    cin.getline(Arr, 30);

    String sobj(Arr);

    sobj.struprX();

    cout << Arr << "\n";

    return 0;
}