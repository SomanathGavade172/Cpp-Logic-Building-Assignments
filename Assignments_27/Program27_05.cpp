class String
{
    public:
        char *str;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : String (Parameterized Constructor)
//  Description      : Initializes data member with given string.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        String(char Arr[])
        {
            str = Arr;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Reverse
//  Description      : Displays the given string in reverse order.
//  Input            : None
//  Output           : Prints reversed string
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Reverse()
        {
            char *Start = str;

            if(str == NULL)
            {
                cout << "Invalid Input\n";
                return;
            }

            while(*str != '\0')
            {                
                str++;
            }

            str--;

            while(str >= Start)
            {
                cout << *str;
                str--;
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Reverse string using OOP
//  Description      : This application accepts string from user and displays it in reverse order.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50];

    cout << "Enter a string : \n";
    cin.getline(Arr, 50);

    String sobj(Arr);

    sobj.Reverse();

    return 0;
}