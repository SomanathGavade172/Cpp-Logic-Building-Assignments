#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : String
//  Description      : Contains function to count number of capital characters in given string.
//  Input            : Character array (string)
//  Output           : Returns count of capital characters
//  Author           : Gavade Somanath Vilas
//  Date             : 14/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class String
{
    public:
        char *str;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : String (Parameterized Constructor)
//  Description      : Initializes data member with given string.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        String(char Arr[])
        {
            str = Arr;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CountCapital
//  Description      : Counts number of capital characters in given string.
//  Input            : None
//  Output           : Integer (count of capital letters)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        int CountCapital()
        {
            int iCount = 0;

            if(str == NULL)
            {
                cout << "Invalid Input\n";
                return;
            }

            while(*str != '\0')
            {
                if(*str >= 'A' && *str <= 'Z')
                {
                    iCount++;
                }
                str++;
            }

            return iCount;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Count capital characters using OOP
//  Description      : This application accepts string from user and counts number of capital characters.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50];
    int iRet = 0;

    cout << "Enter a string : \n";
    cin.getline(Arr, 50);

    String sobj(Arr);

    iRet = sobj.CountCapital();

    cout << "Number of Capital letters are : " << iRet << "\n";

    return 0;
}