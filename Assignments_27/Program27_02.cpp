#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : String
//  Description      : Contains function to count number of small characters in given string.
//  Input            : Character array (string)
//  Output           : Returns count of small characters
//  Author           : Gavade Somanath Vilas
//  Date             : 14/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class String
{
    public:
        char *str;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : String (Parameterized Constructor)
//  Description      : Initializes data member with given string.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        String(char Arr[])
        {
            str = Arr;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CountSmall
//  Description      : Counts number of small characters in given string.
//  Input            : None
//  Output           : Integer (count of small letters)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        int CountSmall()
        {
            int iCount = 0;

            if(str == NULL)
            {
                cout << "Invalid Input\n";
                return;
            }

            while(*str != '\0')
            {
                if(*str >= 'a' && *str <= 'z')
                {
                    iCount++;
                }
                str++;
            }

            return iCount;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Count small characters using OOP
//  Description      : This application accepts string from user and counts number of small characters.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50];
    int iRet = 0;

    cout << "Enter a string : \n";
    cin.getline(Arr, 50);

    String sobj(Arr);

    iRet = sobj.CountSmall();

    cout << "Number of Small letters are : " << iRet << "\n";

    return 0;
}