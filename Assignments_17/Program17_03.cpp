/* 
    3. Write a program which accept number from user and print its numbers line.

    Input  : 4

    Output : -4 -3 -2 -1 0 1 2 3 4

*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Patter
//  Description      : Contains function to print numbers from negative to positive range.
//  Input            : Integer value
//  Output           : Prints numbers line on screen
//  Author           : Gavade Somanath Vilas
//  Date             : 28/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Patter
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Patter (Parameterized Constructor)
//  Description      : Initializes data member with given value.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Patter(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Prints numbers from -N to +N.
//  Input            : None (Uses class data member)
//  Output           : Prints numbers line on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Display()
        {
            int iCnt = 0;

            if(iNo < 0)
            {
                iNo = -iNo;
            }

            for(iCnt = -iNo; iCnt <= iNo; iCnt++)
            {
                cout << iCnt << "\t";
            }
            cout << "\n";
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Print numbers from -N to N using OOP
//  Description      : This application accepts number from user, creates an object of Patter class and prints numbers line.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Patter pobj(iValue);

    pobj.Display();

    return 0;
}