/* 
    4. Write a program which accepts N from user and print all odd numbers up to N.

    Input  : 18

    Output : 1 3 5 7 9 11 13 15 17

*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Patter
//  Description      : Contains function to print all odd numbers up to given number.
//  Input            : Integer value
//  Output           : Prints odd numbers on screen
//  Author           : Gavade Somanath Vilas
//  Date             : 28/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Patter
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Patter (Parameterized Constructor)
//  Description      : Initializes data member with given value.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Patter(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Prints all odd numbers from 1 to N.
//  Input            : None (Uses class data member)
//  Output           : Prints odd numbers on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Display()
        {
            int iCnt = 0;

            if(iNo < 0)
            {
                iNo = -iNo;
            }

            for(iCnt = 1; iCnt <= iNo; iCnt++)
            {
                if(iCnt % 2 != 0)
                {
                    cout << iCnt << "\t";
                }
            }
            cout << "\n";
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Print odd numbers up to N using OOP
//  Description      : This application accepts number from user, creates an object of Patter class and prints odd numbers.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Patter pobj(iValue);

    pobj.Display();

    return 0;
}