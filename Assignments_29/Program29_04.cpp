#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Number
//  Description      : Contains function to display first number second number of times.
//  Input            : Two integers
//  Output           : Prints first number multiple times based on second number
//  Author           : Gavade Somanath Vilas
//  Date             : 27/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Number
{
    public:
        int iNo1;
        int iNo2;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Number (Parameterized Constructor)
//  Description      : Initializes data members with given numbers.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Number(int A, int B)
        {
            iNo1 = A;
            iNo2 = B;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Displays first number second number of times.
//  Input            : None
//  Output           : Prints numbers on screen
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        void Display()
        {
            int iCnt = 0;

            for(iCnt = 1; iCnt <= iNo2; iCnt++)
            {
                cout << iNo1 << "\t";
            }

            cout << "\n";
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Display Number Multiple Times using OOP
//  Description      : This application accepts two numbers and displays the first number second number of times.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue1= 0, iValue2 = 0;

   cout << "Enter a Number : \n";
   cin >> iValue1;

   cout << "Enter a Frequency : \n";
   cin >> iValue2;

    Number nobj(iValue1, iValue2);

    nobj.Display();

    return 0;
}