/*
    4. Accept one number from user and check whether it is Even or Odd.

    Input  : 12
    Output : Given number is Even

    Input  : 7
    Output : Given number is Odd

*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Number
//  Description      : Contains function to check whether given number is even or odd.
//  Input            : Integer
//  Output           : Returns TRUE if even, otherwise FALSE
//  Author           : Gavade Somanath Vilas
//  Date             : 27/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Number
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Number (Parameterized Constructor)
//  Description      : Initializes data member with given number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Number(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : ChkEven
//  Description      : Checks whether given number is even or odd.
//  Input            : None
//  Output           : TRUE if even, FALSE if odd
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        bool ChkEven()
        {
            if(iNo % 2 == 0)
            {
                return true;
            }
            else 
            {
                return false;
            }           
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Even Odd Check using OOP
//  Description      : This application accepts a number and checks whether it is even or odd.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;
   bool bRet = false;

   cout << "Enter a Number : \n";
   cin >> iValue;   

    Number nobj(iValue);

    bRet = nobj.ChkEven();

    if(bRet == true)
    {
        cout << "Given Number is Even\n";
    }
    else
    {
        cout << "Given Number is Odd\n";
    }

    return 0;
}