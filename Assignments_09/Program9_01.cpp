/* 
    1.Write a program which accept radius of circle from user and calculate its area. Consider value of PI as 3.14. (Area = PI * Radius * Radius).

    Input  : 5.3
    Output : 88.2026

    Input  : 10.4
    Output : 339.6224

*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Circle
//  Description      : Contains function to calculate area of a circle.
//  Input            : Float fRadius
//  Output           : Returns area of circle
//  Author           : Gavade Somanath Vilas
//  Date             : 18/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Circle
{
    public:
        float fRadius;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Circle (Parameterized Constructor)
//  Description      : Initializes data member with the given radius value.
//  Input            : Float
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Circle(float A)
        {
            fRadius = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CircleArea
//  Description      : Calculates area of circle using formula (PI * Radius * Radius).
//  Input            : None (Uses class data member)
//  Output           : Returns area of circle
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        double CircleArea()
        {
            double dArea = 0.0;
            float PI = 3.14f;

            dArea = PI * fRadius * fRadius;

            return dArea;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Area of Circle using OOP
//  Description      : This application accepts radius from user, creates an object of Circle class,
//                     and returns the area of the circle.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet = 0.0f;

    cout << "Enter Radius : \n";
    cin >> fValue;

    Circle cobj(fValue);

    dRet = cobj.CircleArea();

    cout << "Area of circle is : " << dRet << "\n";

    return 0;
}