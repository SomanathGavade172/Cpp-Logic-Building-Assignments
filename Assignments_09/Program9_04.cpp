/* 
    4. Write a program which accept temperature in Fahrenheit and convert it into celsius. (1 celsius = (Fahrenheit -32) * (5/9))

    Input  : 10
    Output : -12.2222 (10 - 32) * (5/9)

    Input  : 34
    Output : 1.11111 (34 - 32) * (5/9)

*/ 

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Temperature
//  Description      : Contains function to convert Fahrenheit into Celsius.
//  Input            : Float fTemp
//  Output           : Returns temperature in Celsius
//  Author           : Gavade Somanath Vilas
//  Date             : 18/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Temperature
{
    public:
        float fTemp;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Temperature (Parameterized Constructor)
//  Description      : Initializes data member with given temperature in Fahrenheit.
//  Input            : Float
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Temperature(float A)
        {
            fTemp = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : FhToCs
//  Description      : Converts Fahrenheit into Celsius using formula (C = (F - 32) * (5/9)).
//  Input            : None (Uses class data member)
//  Output           : Returns temperature in Celsius
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        double FhToCs()
        {
            double dCelsius = 0.0;

            dCelsius = (fTemp - 32) * (5.0f / 9.0f);  

            return dCelsius;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Fahrenheit to Celsius Conversion using OOP
//  Description      : This application accepts temperature in Fahrenheit from user, creates an object of Temperature class,
//                     and returns the temperature in Celsius.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet = 0.0f;

    cout << "Enter Temperature in Fahrenheit : \n";
    cin >> fValue;

    Temperature dobj(fValue);

    dRet = dobj.FhToCs();

    cout << fValue << " Fahrenheit is = " << dRet << " Celsius.\n";

    return 0;
}