/*
    2. Write a program which accept one number and position from user and off that bit. Return modified number.

    Input  : 10 2
    Output : 8 
    
*/

#include<iostream>
using namespace std;

typedef unsigned int UINT;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Bit
//  Description      : Contains function to turn OFF bit at given position of a number.
//  Input            : Unsigned Integer and Position
//  Output           : Returns modified number after turning OFF specified bit
//  Author           : Gavade Somanath Vilas
//  Date             : 05/05/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Bit
{
    public:
        UINT iNo;
        UINT iPos;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Bit (Parameterized Constructor)
//  Description      : Initializes data members with given number and position.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Bit(UINT A, UINT B)
        {
            iNo = A;
            iPos = B;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : OffBit
//  Description      : Turns OFF bit at specified position.
//  Input            : None
//  Output           : Modified number with specified bit OFF
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        UINT OffBit()
        {
            UINT iMask = 0x1;
            UINT iResult = 0;

            iMask = iMask << (iPos - 1);

            iMask = ~iMask;

            iResult = iNo & iMask;

            return iResult;          
        }        
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Turn OFF Bit at Given Position using OOP
//  Description      : This application accepts a number and position and turns OFF that bit.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iLocation = 0;
    int iRet = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    cout << "Enter a Position : \n";
    cin >> iLocation;

    Bit bobj(iValue, iLocation);

    iRet = bobj.OffBit();

    cout << "Number after bit off : " << iRet << "\n";

    return 0;
}