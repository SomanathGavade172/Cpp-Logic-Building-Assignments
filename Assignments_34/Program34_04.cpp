/* 
    4. Write a program which accept one number and position from user and toggle that bit. Return modified number.

    Input  : 10 3
    Output : 14
    
*/


#include<iostream>
using namespace std;

typedef unsigned int UINT;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Bit
//  Description      : Contains function to toggle bit at given position of a number.
//  Input            : Unsigned Integer and Position
//  Output           : Returns modified number after toggling specified bit
//  Author           : Gavade Somanath Vilas
//  Date             : 05/05/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Bit
{
    public:
        UINT iNo;
        UINT iPos;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Bit (Parameterized Constructor)
//  Description      : Initializes data members with given number and position.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Bit(UINT A, UINT B)
        {
            iNo = A;
            iPos = B;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : ToggleBit
//  Description      : Toggles bit at specified position.
//  Input            : None
//  Output           : Modified number with specified bit toggled
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        UINT ToggleBit()
        {
            UINT iMask = 0x1;
            UINT iResult = 0;

            iMask = iMask << (iPos - 1);

            iResult = iNo ^ iMask;

            return iResult;          
        }        
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Toggle Bit at Given Position using OOP
//  Description      : This application accepts a number and position and toggles that bit.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iLocation = 0;
    int iRet = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    cout << "Enter a Position : \n";
    cin >> iLocation;

    Bit bobj(iValue, iLocation);

    iRet = bobj.ToggleBit();

    cout << "Number after bit Toggle : " << iRet << "\n";

    return 0;
}