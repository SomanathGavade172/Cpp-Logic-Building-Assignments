/* 
    5. Write a program which accept one number from user and toggle contents of first and last nibble of the number. Return modified number. (Nibble is a group of four bits).
    
*/

#include<iostream>
using namespace std;

typedef unsigned int UINT;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Bit
//  Description      : Contains function to toggle first and last nibble of a number.
//  Input            : Unsigned Integer
//  Output           : Returns modified number after toggling specified nibbles
//  Author           : Gavade Somanath Vilas
//  Date             : 05/05/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Bit
{
    public:
        UINT iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Bit (Parameterized Constructor)
//  Description      : Initializes data member with given number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Bit(UINT A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : ToggleBit
//  Description      : Toggles contents of first and last nibble.
//  Input            : None
//  Output           : Modified number with first and last nibble toggled
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        UINT ToggleBit()
        {
            UINT iMask = 0xF000000F;
            UINT iResult = 0;

            iResult = iNo ^ iMask;

            return iResult;          
        }        
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Toggle First and Last Nibble using OOP
//  Description      : This application accepts a number and toggles its first and last nibble.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Bit bobj(iValue);

    iRet = bobj.ToggleBit();

    cout << "Number after nibble Toggle : " << iRet << "\n";

    return 0;
}