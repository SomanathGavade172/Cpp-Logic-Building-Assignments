#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Character
//  Description      : Contains function to check whether given character is a capital letter or not.
//  Input            : Character
//  Output           : Returns TRUE if capital letter, otherwise FALSE
//  Author           : Gavade Somanath Vilas
//  Date             : 28/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Character
{
    public:
        char Ch;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Character (Parameterized Constructor)
//  Description      : Initializes data member with given character.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Character(char c)
        {
            Ch = c;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : ChkCapital
//  Description      : Checks whether given character is a capital letter or not.
//  Input            : None
//  Output           : TRUE if capital letter, FALSE otherwise
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        bool ChkCapital()
        {
            if(Ch >= 'A' && Ch <= 'Z')
            {
                return true;
            }

            return false;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Capital Character Check using OOP
//  Description      : This application accepts a character and checks whether it is a capital letter or not.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    bool bRet = false;

    cout << "Enter a Character : \n";
    cin >> cValue;

    Character cobj(cValue);

    bRet = cobj.ChkCapital();

    if(bRet == true)
    {
        cout << "TRUE\n";
    }
    else
    {
        cout << "FALSE\n";
    }

    return 0;
}