/* 
    1. Accept Character from user and check whether it is alphabet or not (A-Z a-z).

    Input  : F
    Output : TRUE

    Input  : &
    Output : FALSE

*/

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Character
//  Description      : Contains function to check whether given character is an alphabet or not.
//  Input            : Character
//  Output           : Returns TRUE if alphabet, otherwise FALSE
//  Author           : Gavade Somanath Vilas
//  Date             : 28/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Character
{
    public:
        char Ch;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Character (Parameterized Constructor)
//  Description      : Initializes data member with given character.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Character(char c)
        {
            Ch = c;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CheckAlpa
//  Description      : Checks whether given character is an alphabet or not.
//  Input            : None
//  Output           : TRUE if alphabet, FALSE otherwise
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        bool CheckAlpa()
        {
            if((Ch >= 'A' && Ch <= 'Z') || (Ch >= 'a' && Ch <= 'z'))
            {
                return true;
            }

            return false;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Alphabet Check using OOP
//  Description      : This application accepts a character and checks whether it is an alphabet or not.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    bool bRet = false;

    cout << "Enter a Character : \n";
    cin >> cValue;

    Character cobj(cValue);

    bRet = cobj.CheckAlpa();

    if(bRet == true)
    {
        cout << "TRUE\n";
    }
    else
    {
        cout << "FALSE\n";
    }

    return 0;
}