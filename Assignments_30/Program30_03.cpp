#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Character
//  Description      : Contains function to check whether given character is a digit or not.
//  Input            : Character
//  Output           : Returns TRUE if digit, otherwise FALSE
//  Author           : Gavade Somanath Vilas
//  Date             : 29/04/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Character
{
    public:
        char Ch;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Character (Parameterized Constructor)
//  Description      : Initializes data member with given character.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Character(char c)
        {
            Ch = c;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : ChkDigit
//  Description      : Checks whether given character is a digit or not.
//  Input            : None
//  Output           : TRUE if digit, FALSE otherwise
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        bool ChkDigit()
        {
            if(Ch >= '0' && Ch <= '9')
            {
                return true;
            }

            return false;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Digit Character Check using OOP
//  Description      : This application accepts a character and checks whether it is a digit or not.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    bool bRet = false;

    cout << "Enter a Character : \n";
    cin >> cValue;

    Character cobj(cValue);

    bRet = cobj.ChkDigit();

    if(bRet == true)
    {
        cout << "TRUE\n";
    }
    else
    {
        cout << "FALSE\n";
    }

    return 0;
}