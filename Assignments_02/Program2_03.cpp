// 3. Accept one number from user if number is less than 10 then print "Hello" otherwise print "Demo".

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Number
//  Description      : Contains function to check the number and display "Hello" or "Demo" based on condition.
//  Input            : Integer iNo - Number to be checked
//  Output           : None (Prints result on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 09/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Number
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Number (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : One integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Number(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Checks whether the stored integer is less than 10 and prints "Hello",
//                     otherwise prints "Demo".
//  Input            : None (Uses class data member)
//  Output           : None (Prints message on console)
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition

        void Display()
        {
            // Updator
            if(iNo < 0)
            {
                iNo = -(iNo);
            }

            if(iNo < 10)
            {
                cout << "Hello\n";
            }
            else
            {
                cout << "Demo\n";
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Check Number Condition using OOP
//  Description      : This application accepts an integer from user, creates an object of Number class,
//                     and prints "Hello" if number is less than 10 otherwise prints "Demo".
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    // Create a object of a Class
    Number nobj(iValue);

    nobj.Display();         //  Function Call

    return 0;
}