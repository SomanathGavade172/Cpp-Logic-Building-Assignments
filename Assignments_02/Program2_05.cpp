// 5. Accept number from user and check whether number is even or odd.

#include<iostream>
using namespace std;

typedef int BOOL;

#define TRUE 1
#define FALSE 0

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Number
//  Description      : Contains function to check whether the given number is even or odd.
//  Input            : Integer iNo - Number to be checked
//  Output           : TRUE if number is even otherwise FALSE
//  Author           : Gavade Somanath Vilas
//  Date             : 09/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Number
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Number (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : One integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Number(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CheckEvenOdd
//  Description      : Checks whether the stored integer is even or odd.
//  Input            : None (Uses class data member)
//  Output           : TRUE if even otherwise FALSE
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition

        BOOL CheckEvenOdd()
        {
            if(iNo % 2 == 0)
            {
                return TRUE;
            }
            else
            {
                return FALSE;
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Check Even or Odd using OOP
//  Description      : This application accepts an integer from user, creates an object of Number class,
//                     and checks whether the number is even or odd.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    BOOL bRet = FALSE;

    cout << "Enter a Number : \n";
    cin >> iValue;

    // Create a object of a Class
    Number nobj(iValue);

    bRet = nobj.CheckEvenOdd();         //  Function Call

    if(bRet == TRUE)
    {
        cout << "Number is Even\n";
    }
    else
    {
        cout << "Number is Odd\n";
    }

    return 0;
}