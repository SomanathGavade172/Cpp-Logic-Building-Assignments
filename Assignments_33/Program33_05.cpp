/* 
    5. Write a program which accept one number from user and on its first 4 bits. Return modified number.

    Input  : 73
    Output : 79
    
*/

#include<iostream>
using namespace std;

typedef unsigned int UINI;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Bit
//  Description      : Contains function to turn ON first 4 bits of a number.
//  Input            : Unsigned Integer
//  Output           : Returns modified number after turning ON first 4 bits
//  Author           : Gavade Somanath Vilas
//  Date             : 04/05/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Bit
{
    public:
        UINI iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Bit (Parameterized Constructor)
//  Description      : Initializes data member with given number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Bit(UINI A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : BitOn
//  Description      : Turns ON first 4 bits of the number.
//  Input            : None
//  Output           : Modified number with first 4 bits ON
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        UINI BitOn()
        {
            UINI uResult = 0;
            UINI iMask = 0xF;

            uResult = iNo | iMask;

            return uResult;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Turn ON First 4 Bits using OOP
//  Description      : This application accepts a number and turns ON its first 4 bits.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINI iValue = 0;
    UINI iRet = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Bit bobj(iValue);

    iRet = bobj.BitOn();

    cout << "Number after first 4 bits are ON : " << iRet << "\n";

    return 0;
}