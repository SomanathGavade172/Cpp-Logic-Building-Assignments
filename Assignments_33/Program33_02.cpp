/*
    2. Write a program which accept one number from user and off 7th and 10th bit of that number. Return modified number.

        Input  : 577
        Output : 1
        
*/

#include<iostream>
using namespace std;

typedef unsigned int UINI;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Bit
//  Description      : Contains function to turn OFF 7th and 10th bits of a number.
//  Input            : Unsigned Integer
//  Output           : Returns modified number after turning OFF specified bits
//  Author           : Gavade Somanath Vilas
//  Date             : 04/05/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Bit
{
    public:
        UINI iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Bit (Parameterized Constructor)
//  Description      : Initializes data member with given number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Bit(UINI A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : BitOff
//  Description      : Turns OFF 7th and 10th bits of the number.
//  Input            : None
//  Output           : Modified number with 7th and 10th bits OFF
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        UINI BitOff()
        {
            UINI uResult = 0;
            UINI iMask = 0xFFFFFDBF;

            uResult = iNo & iMask;

            return uResult;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Turn OFF 7th and 10th Bits using OOP
//  Description      : This application accepts a number and turns OFF its 7th and 10th bits.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINI iValue = 0;
    UINI iRet = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Bit bobj(iValue);

    iRet = bobj.BitOff();

    cout << "Number after 7th and 10th bit is off : " << iRet << "\n";

    return 0;
}