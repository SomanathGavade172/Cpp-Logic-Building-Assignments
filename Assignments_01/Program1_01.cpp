// 1.Program to divide two numbers

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Arithmetic
//  Description      : Performs arithmetic operations on two integers.
//  Input            : Integer iNo1 - First number
//                     Integer iNo2 - Second number
//  Output           : Integer (Result of division)
//  Author           : Gavade Somanath Vilas
//  Date             : 08/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Arithmetic
{
    public:
        int iNo1, iNo2;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Arithmetic (Parameterized Constructor)
//  Description      : Initializes data members with the given integer values.
//  Input            : Two integers
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Arithmetic(int A, int B)
        {
            iNo1 = A;
            iNo2 = B;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Divide
//  Description      : Performs division of first number by second number.
//  Input            : None (Uses class data members)
//  Output           : Integer (Result of division or -1 if division by zero)
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        int Divide()
        {
            int iAns = 0;

            if(iNo2 == 0)
            {
                return -1;
            }

            iAns = iNo1 / iNo2;

            return iAns;
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Division of Two Numbers using OOP
//  Description      : This application accepts two integers from user, creates an object of Arithmetic class, and performs division.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0, iValue2 = 0;
    int iRet = 0;

    cout << "Enter First Number : \n";
    cin >> iValue1;

    cout << "Enter Second Number : \n";
    cin >> iValue2;

    // Create a Object of a class
    Arithmetic aobj(iValue1, iValue2);

    iRet = aobj.Divide();       // Function Call.

    cout << "Division is : " << iRet << "\n";

    return 0;
}