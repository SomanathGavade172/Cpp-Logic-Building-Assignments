// 2. Program to print 5 times “Marvellous” on screen.

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Arithmetic
//  Description      : Contains function to display the word "Marvellous" multiple times on screen.
//  Input            : None
//  Output           : None (Prints output on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 08/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Arithmetic
{
    public:

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Prints the word "Marvellous" five times on the screen using loop.
//  Input            : None
//  Output           : None (Prints string on console)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        void Display()
        {
            int iCnt = 0;

            for(iCnt = 1; iCnt <= 5; iCnt++)
            {
                cout << "Marvellous" << "\n";
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Print String Multiple Times.
//  Description         :   This application prints the word "Marvellous" five times using loop.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Arithmetic aobj;

    aobj.Display();

    return 0;
}