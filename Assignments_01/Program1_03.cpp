// 3. Program to print 5 to 1 numbers on screen.

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Number
//  Description      : Contains function to display numbers from 5 to 1 on the screen.
//  Input            : None
//  Output           : None (Prints numbers on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 08/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Number
{
    public:

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : Display
//  Description      : Prints numbers from 5 to 1 on the screen using loop.
//  Input            : None
//  Output           : None (Prints numbers on console)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        void Display()
        {
            int iCnt = 0;

            for(iCnt = 5; iCnt >= 1; iCnt--)
            {
                cout << iCnt << "\t";
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Print Numbers in Reverse Order.
//  Description         :   This application prints numbers from 5 to 1 using loop.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Number nobj;

    nobj.Display();

    return 0;
}