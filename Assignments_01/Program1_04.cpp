// 4. Accept one number and check whether it is divisible by 5 or not.

#include<iostream>
using namespace std;

typedef int BOOL;

#define TRUE 1
#define FALSE 0

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Number
//  Description      : Contains function to check whether the given number is divisible by 5 or not.
//  Input            : Integer iNo - Number to be checked
//  Output           : TRUE if number is divisible by 5, otherwise FALSE
//  Author           : Gavade Somanath Vilas
//  Date             : 08/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Number
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Number (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : One integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Number(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : CheckDivision
//  Description      : Checks whether the stored integer is divisible by 5 or not.
//  Input            : None (Uses class data member)
//  Output           : TRUE if divisible by 5, otherwise FALSE
//  Time Complexity  : O(1)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        BOOL CheckDivision()
        {
            if(iNo % 5 == 0)
            {
                return TRUE;
            }
            else
            {
                return FALSE;
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Check Divisibility of Number by 5 using OOP
//  Description      : This application accepts an integer from user, creates an object of Number class, and checks whether the number is divisible by 5.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    BOOL bRet = FALSE;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Number nobj(iValue);

    bRet = nobj.CheckDivision();

    if(bRet == TRUE)
    {
        cout << "It is Divisible by 5" << "\n";
    }
    else
    {
        cout << "It is Not Divisible by 5" <<"\n";
    }

    return 0;
}