// 2. Write a program which accept number from user and print even factor of that number.

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Number
//  Description      : Contains function to print even factors of the given number.
//  Input            : Integer iNo - Number whose even factors are to be printed
//  Output           : None (Prints even factors on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 11/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Number
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Number (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : One integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Number(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : PrintEven
//  Description      : Prints even factors of the stored integer value.
//  Input            : None (Uses class data member)
//  Output           : None (Prints even factors on console)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        void DisplayFactor()
        {
            int iCnt = 0;

            if(iNo < 0)
            {
                iNo = -(iNo);
            }

            for(iCnt = 1; iCnt <= iNo / 2; iCnt++)
            {
                if(iNo % iCnt == 0 && iCnt % 2 == 0)
                {
                    cout << iCnt << "\t";
                }
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Print Even Factors using OOP
//  Description      : This application accepts an integer from user, creates an object of Number class,
//                     and prints even factors of the given number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Number nobj(iValue);

    nobj.DisplayFactor();

    return 0;
}