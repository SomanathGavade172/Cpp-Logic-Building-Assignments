// 1. Write a program which accept one number from user and print that number of even numbers on screen.

#include<iostream>
using namespace std;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name       : Number
//  Description      : Contains function to print even numbers on the screen as per the given number.
//  Input            : Integer iNo - Number of even numbers to be printed
//  Output           : None (Prints even numbers on console)
//  Author           : Gavade Somanath Vilas
//  Date             : 11/03/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Number
{
    public:
        int iNo;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Constructor Name : Number (Parameterized Constructor)
//  Description      : Initializes data member with the given integer value.
//  Input            : One integer
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Number(int A)
        {
            iNo = A;
        }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name    : PrintEven
//  Description      : Prints even numbers on the screen equal to the stored integer value.
//  Input            : None (Uses class data member)
//  Output           : None (Prints even numbers on console)
//  Time Complexity  : O(N)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

        // Function Definition
        void PrintEven()
        {
            int iCnt = 0;

            for(iCnt = 1; iCnt <= iNo; iCnt++)
            {
                cout << 2 * iCnt << "\t";
            }
        }
};

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name : Print Even Numbers using OOP
//  Description      : This application accepts an integer from user, creates an object of Number class,
//                     and prints that many even numbers on the screen.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter a Number : \n";
    cin >> iValue;

    Number nobj(iValue);

    nobj.PrintEven();

    return 0;
}